def trim_nmap_output():
    input_file = input("Enter the path of the input file to trim: ")
    output_file = input("Enter the path and name of the output file for the trimmed version: ")

    with open(input_file, 'r') as f:
        lines = f.readlines()

    trimmed_lines = []
    is_host_section = False

    for line in lines:
        line = line.strip()

        # Check if it is a host section
        if line.startswith("Nmap scan report for"):
            is_host_section = True
        elif line.startswith("Host is up") or line.startswith("Note: Host seems down"):
            is_host_section = False

        # Add line if it is not a host section, and not a time stamp message
        if not is_host_section and not line.startswith("Nmap done:"):
            trimmed_lines.append(line)

    with open(output_file, 'w') as f:
        f.write('\n'.join(trimmed_lines))

    print("Trimmed output written to", output_file)

# Usage
trim_nmap_output()
